package dev.kalenchukov.notation.converting.resources;

/**
 * Перечисление возможных разделителей нотаций.
 */
public enum Separator
{
	/**
	 * Нет разделителя.
	 */
	NONE,

	/**
	 * Символ дефиса.
	 */
	HYPHEN,

	/**
	 * Символ нижнего подчёркивания.
	 */
	UNDERSCORE,

	/**
	 * Прописная буква.
	 */
	UPPERCASE;
}
